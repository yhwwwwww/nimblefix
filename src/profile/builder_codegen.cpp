auto
DefaultNamespaceName(const NormalizedDictionary& dictionary) -> std::string
{
  return "nimble::generated::profile_" + std::to_string(dictionary.profile_id);
}

auto
CamelToSnake(std::string_view name) -> std::string
{
  std::string result;
  result.reserve(name.size() + 4);
  for (std::size_t i = 0; i < name.size(); ++i) {
    const auto ch = name[i];
    if (std::isupper(static_cast<unsigned char>(ch))) {
      if (i > 0 && std::islower(static_cast<unsigned char>(name[i - 1]))) {
        result.push_back('_');
      } else if (i > 0 && i + 1 < name.size() && std::isupper(static_cast<unsigned char>(name[i - 1])) &&
                 std::islower(static_cast<unsigned char>(name[i + 1]))) {
        result.push_back('_');
      }
      result.push_back(static_cast<char>(std::tolower(static_cast<unsigned char>(ch))));
    } else {
      result.push_back(ch);
    }
  }
  return result;
}

auto
IsEncodeManagedTag(std::uint32_t tag) -> bool
{
  return nimble::codec::tags::IsEncodeManagedTag(tag);
}

auto
SetterMethodSuffix(ValueType type) -> std::string_view
{
  switch (type) {
    case ValueType::kString:
      return "string";
    case ValueType::kInt:
      return "int";
    case ValueType::kChar:
      return "char";
    case ValueType::kFloat:
      return "float";
    case ValueType::kBoolean:
      return "boolean";
    case ValueType::kTimestamp:
      return "string";
    default:
      return "string";
  }
}

auto
SetterArgType(ValueType type) -> std::string_view
{
  switch (type) {
    case ValueType::kString:
      return "std::string_view";
    case ValueType::kInt:
      return "std::int64_t";
    case ValueType::kChar:
      return "char";
    case ValueType::kFloat:
      return "double";
    case ValueType::kBoolean:
      return "bool";
    case ValueType::kTimestamp:
      return "std::string_view";
    default:
      return "std::string_view";
  }
}

auto
GetterReturnType(ValueType type) -> std::string_view
{
  switch (type) {
    case ValueType::kString:
      return "std::optional<std::string_view>";
    case ValueType::kInt:
      return "std::optional<std::int64_t>";
    case ValueType::kChar:
      return "std::optional<char>";
    case ValueType::kFloat:
      return "std::optional<double>";
    case ValueType::kBoolean:
      return "std::optional<bool>";
    case ValueType::kTimestamp:
      return "std::optional<std::string_view>";
    default:
      return "std::optional<std::string_view>";
  }
}

auto
GetterMethodName(ValueType type) -> std::string_view
{
  switch (type) {
    case ValueType::kString:
      return "get_string";
    case ValueType::kInt:
      return "get_int";
    case ValueType::kChar:
      return "get_char";
    case ValueType::kFloat:
      return "get_float";
    case ValueType::kBoolean:
      return "get_boolean";
    case ValueType::kTimestamp:
      return "get_string";
    default:
      return "get_string";
  }
}

